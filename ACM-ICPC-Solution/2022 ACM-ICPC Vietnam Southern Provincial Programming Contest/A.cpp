void sol() {
    long long n;
    cin >> n;

    long long win = 0, lose = ((n + 1) / 2) * ((n + 1) / 2);
    for (int i = 0; i <= 15; i++) {
        int k = 2 * i + 1;
        if ((1LL << k) > n) break;

        long long t = (n / (1LL << k));
        win += t * t - (t / 2) * (t / 2);
    }

    for (int i = 1; i <= 15; i++) {
        int k = 2 * i;
        if ((1LL << k) > n) break;

        long long t = ((n / (1LL << k)) - 1) / 2 + 1;
        lose += t * t;
    }

    cout << win << "\n"
         << n * n - win - lose << "\n"
         << lose;
}

